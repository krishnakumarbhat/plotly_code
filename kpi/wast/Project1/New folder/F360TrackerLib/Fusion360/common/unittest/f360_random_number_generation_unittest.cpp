/** \purpose
 * Verify that numbers sampled from the random generator with different seeds are different.
 * Since the random number generator is pseudorandom, it's enough to check that for two seeds the
 * first drawn number is different.
 * \req
 * NA
 */
TEST(f360_random_number_generation, Generate_Random_Number_different_samples_for_different_seeds)
{
   /** \action
    * - Set the random seed
    * - Draw a number
    * - Set a different seed
    * - Draw another number
    */
   Init_Random_Number_Generator(1);
   uint32_t draw_1 = Generate_Random_Number();
   Init_Random_Number_Generator(0);
   uint32_t draw_2 = Generate_Random_Number();

   /** \result
    * The drawn numbers must be different.
    */
   char text_failure[100];
   sprintf(text_failure, "%d and %d are not different\n", draw_1, draw_2);
   CHECK_TRUE_TEXT(draw_1 != draw_2, text_failure);
}

/** \purpose
 * Verify that numbers sampled from the random generator with the same seeds are the same.
 * \req
 * NA
 */
TEST(f360_random_number_generation, Generate_Random_Number_same_sample_for_same_seed)
{
   /** \precond
    * - Random number generator seed = 1
    * - Number of draws = 1e5
    */
   uint32_t seed = 1;
   const static size_t num_of_draws = static_cast<size_t>(1e5);

   /** \action
    * - Set random number generator seed.
    * - Draw random numbers and store them in an array.
    * - Set the seed back to the initial value.
    * - Draw random numbers and store them in another array.
    */
   Init_Random_Number_Generator(seed);
   std::array<uint32_t, num_of_draws> draws_0;
   for (size_t i = 0; i < num_of_draws; i++)
   {
      draws_0[i] = Generate_Random_Number();
   }

   Init_Random_Number_Generator(seed);
   std::array<uint32_t, num_of_draws> draws_1;
   for (size_t i = 0; i < num_of_draws; i++)
   {
      draws_1[i] = Generate_Random_Number();
   }

   /** \result
    * The arrays of random numbers must match exactly.
    */
   char text_failure[100];
   bool are_equal = std::equal(std::begin(draws_0), std::end(draws_0), std::begin(draws_1));
   sprintf(text_failure, "samples generated by the same seed are different\n");
   CHECK_TRUE_TEXT(are_equal, text_failure);
}

