/** \purpose
 * Verify indices sampled from the random generator are within the expected bounds.
 * Seed = 1.
 * \req
 * NA
 */
TEST(f360_random_number_generation, Generate_Random_Index_range_seed_1)
{
   /** \precond
    * - Maximum index to draw (max_index) = 10
    * - Number of draws = 1e5
    * - Initialize random number generator with seed = 1
    */
   uint32_t max_index = 10;
   const static size_t num_of_draws = static_cast<size_t>(1e5);
   Init_Random_Number_Generator(1);

   uint32_t value = 0;
   char text_failure[100];
   for (size_t i = 0; i < num_of_draws; i++)
   {
      /** \action
       * Draw from the uniform distribution
       */
      value = Generate_Random_Index(max_index);

      /** \result
       * The drawn value is within the interval [0, max_index].
       */
      sprintf(text_failure, "value (%d) is not within the expected bounds: [%d, %d]\n", value, 0, max_index);
      CHECK_TRUE_TEXT(max_index >= value, text_failure);
   }
}

