/** \purpose
 * Verify the mean and variance of a large sample of random indices.
 * \req
 * NA
 */
TEST(f360_random_number_generation, Generate_Random_Index_moments)
{
   /** \precond
    * - Maximum index to draw (max_index) = 10
    * - Number of draws = 1e5
    * - Initialize random number generator with seed = 1
    */
   uint32_t max_index = 10;
   const static size_t num_of_draws = static_cast<size_t>(1e5);
   Init_Random_Number_Generator(1);

   /** \action
    * - Draw a large sample of random indices.
    * - Compute sample mean.
    * - Compute sample variance.
    */
   uint32_t sum = 0;
   std::array<uint32_t, num_of_draws> draws;
   for (size_t i = 0; i < draws.size(); i++)
   {
      draws[i] = Generate_Random_Index(max_index);
      sum += draws[i];
   }

   // Compute mean
   float32_t mean = static_cast<float32_t>(sum) / static_cast<float32_t>(draws.size());
   
   // Compute variance
   float32_t diff_sq = 0.0f;
   for (auto draw : draws)
   {
      diff_sq += std::pow(draw - mean, 2.0f);
   }   
   float32_t variance = diff_sq / static_cast<float32_t>(draws.size() - 1);

   /** \result
    * The sample mean and variance must not exceed a difference of 1% from the expected mean and variance.
    * - Expected mean: max_index / 2
    * - Expected variance: ((max_index + 1)^2 - 1) / 12
    */
   float32_t expected_mean = max_index / 2.0f;
   float32_t expected_var = (static_cast<float32_t>(std::pow(max_index + 1, 2.0f) - 1)) / 12.0f;
   float32_t threshold_ratio = 0.01f;
   DOUBLES_EQUAL_TEXT(expected_mean, mean, threshold_ratio * expected_mean, "sample mean is not within the allowed range\n");
   DOUBLES_EQUAL_TEXT(expected_var, variance, threshold_ratio * expected_var, "sample variance is not within the allowed range\n");
}
